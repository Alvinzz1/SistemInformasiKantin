     */
    public function index()
    {
        $userId = Auth::id();
        $user = User::with('carts')->find($userId);
        $details = Transaction::where('user_id', auth()->id())->get();
        return view('dashboard.riwayat.index', [
            'carts' => $user->carts,
            'histories' => $details,
            'detail' => History::whereIn('transaction_id', $details->pluck('id'))->get(),
        ]);
    }
    
