    public function indexs()
    {
        $users = History::select(DB::raw("COUNT(*) as count"), DB::raw("MONTHNAME(created_at) as month_name"))
        ->whereYear('created_at', date('Y'))
        ->groupBy(DB::raw("month_name"))
        ->orderBy('id','ASC')
        ->pluck('count', 'month_name');

    $labels = $users->keys();
    $data = $users->values();

        return view('dashboard.home.index',compact('labels', 'data'));
    }


